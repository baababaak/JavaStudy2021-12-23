package a02_변수;

public class StringEx {

	public static void main(String[] args) {
		char lastName = '박';
		char firstName1 = '지';
		char firstName2 = '현';
		System.out.println("" + lastName + firstName1 + firstName2); // 자동형변환(업캐스팅)
		
		// 문자열 자료형
		// String은 클래스
		String name = "박지현";
		System.out.println(name);

	}
 
}
